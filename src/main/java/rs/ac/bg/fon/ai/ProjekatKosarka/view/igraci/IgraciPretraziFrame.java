    @Override
    public void itemStateChanged(ItemEvent e) {
         if (e.getSource() == checkObrisi) {
            // If checkbox1 is checked, disable checkbox2
            if (checkObrisi.isSelected()) {
                checkIzmeni.setSelected(false);
            } 
        } else if (e.getSource() == checkIzmeni) {
            // If checkbox2 is checked, disable checkbox1
            if (checkIzmeni.isSelected()) {
                checkObrisi.setSelected(false);
            } 
        }
    }
