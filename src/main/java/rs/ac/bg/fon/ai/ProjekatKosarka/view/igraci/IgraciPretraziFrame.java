    private void btnFiltrirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrirajActionPerformed
        try {
            String ime = null;
            String prezime = null;
            Double visina = null;
            Integer broj = null;
            Pozicija pozicija = null;
            if (!txtIme.getText().isBlank()) {
                ime = txtIme.getText();
            }
            if (!txtPrezime.getText().isBlank()) {
                prezime = txtPrezime.getText();
            }
            if (!txtVisina.getText().isBlank()) {
                visina = Double.valueOf(txtVisina.getText());
            }
            if (!txtBroj1.getText().isBlank()) {
                broj = Integer.valueOf(txtBroj1.getText());
            }

         
            pozicija = Pozicija.valueOf(String.valueOf(cmbPozicija.getSelectedItem()));
              igrac.setBroj(broj);
            igrac.setPozicija(pozicija);
            igrac.setIme(ime);
            igrac.setPrezime(prezime);
            igrac.setVisina(visina);
            List<Igraci> mojiIgraci = controller.vratiSveIgraceFiltrirane(igrac);
            if(mojiIgraci!=null){
                    modelTabele.setListaIgraci(mojiIgraci);
                    modelTabele.fireTableDataChanged();
            
            }else{
                JOptionPane.showMessageDialog(this, "Nije pronadjen nijedan igrac sa zadatim filterom");
            }
        

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnFiltrirajActionPerformed

    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tblIgraci.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita clana","Obavestenje",JOptionPane.WARNING_MESSAGE);
            dispose();
            return;
        }
        
        igrac = modelTabele.getListaIgraci().get(tblIgraci.getSelectedRow());
        promeniIgracaFrame = applicationContext.getBean(PromeniIgracaFrame.class);
        if(checkObrisi.isSelected())
             promeniIgracaFrame.setMode(true);
        if(checkIzmeni.isSelected())
            promeniIgracaFrame.setMode(false);
        promeniIgracaFrame.setIgrac(igrac);
        
        modelTabele.fireTableDataChanged();
        promeniIgracaFrame.setVisible(true);
        
    }//GEN-LAST:event_jButton1ActionPerformed

